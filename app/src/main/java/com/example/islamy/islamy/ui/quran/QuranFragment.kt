    private fun initiateRecyclerView() {
        adapter = SuraRecyclerAdapter(suwarName)
        adapter.onItemClickListner = SuraRecyclerAdapter.OnItemClickListner { item, postion ->
            startSura(item, postion)
        }
        binding.recyclerQuran.adapter = adapter
    }

    private fun startSura(title: String, position: Int) {
        val intent = Intent(activity, SuraDetailsActivity::class.java)
        intent.putExtra(Constant.SuraTitle,title)
        intent.putExtra(Constant.Surapostion,position+1)
        startActivity(intent)
